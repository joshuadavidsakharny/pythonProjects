# python program / surface area, volume of cube

# input variables
side = int(input('Cube Side Length: '))

# calculate surface area
surfaceArea = (side ** 2) * 6

# calculate volume
volume = side ** 3

# output
print("The Surface Area of The Cube is: " + str(surfaceArea)
      + ". The Volume Area of The Cube: " + str(volume))
